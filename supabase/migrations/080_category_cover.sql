-- ============================================================
-- 080 — обложка плитки категории на гостевой странице.
--
-- До сих пор плитка категории в онлайн-меню брала фото ПЕРВОГО товара
-- категории. Теперь у категории есть своя обложка cover_url: владелец
-- ставит любую картинку (загружает в бакет menu-images, как фото товара,
-- или выбирает фото одного из товаров). Пусто/NULL = прежнее поведение
-- (фото первого товара), поэтому старые точки ничего не теряют.
--
-- Только колонка. RLS menu_categories (003, FOR ALL) и GRANT ALL (071)
-- уже покрывают новую колонку — отдельных политик/грантов не нужно.
-- Старый клиент колонку не читает и не пишет — совместимость сохраняется.
-- ============================================================

ALTER TABLE menu_categories ADD COLUMN cover_url TEXT;
