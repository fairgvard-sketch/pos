-- pgTAP: строгий режим require_staff_perm (090).
--
-- Регресс, который закрывает 090: 055 переопределила функцию телом 044
-- и вернула мягкую ветку поверх строгой 045 — вызов без токена проходил.
-- Тест фиксирует строгость, чтобы следующее переопределение не откатило
-- её так же молча.
--
-- Второй инвариант: 089 пускает владельца бэкофиса ДО обращения к
-- require_staff_perm, поэтому строгий режим не должен ломать «Обзор» в
-- веб-кабинете, где PIN-сессии нет по определению.

BEGIN;
SELECT plan(9);

-- ── Фикстура: организация, точка, менеджер, бариста, сессии ──
INSERT INTO orgs (id, name)
VALUES ('70000000-0000-4000-8000-000000000001', 'pgTAP org S');

INSERT INTO locations (id, org_id, name)
VALUES ('71000000-0000-4000-8000-000000000001',
        '70000000-0000-4000-8000-000000000001', 'Loc S');

INSERT INTO staff (id, org_id, location_id, name, role, pin_hash) VALUES
  ('72000000-0000-4000-8000-000000000001', '70000000-0000-4000-8000-000000000001',
   '71000000-0000-4000-8000-000000000001', 'pgTAP manager', 'manager', 'unused-in-test'),
  ('72000000-0000-4000-8000-000000000002', '70000000-0000-4000-8000-000000000001',
   '71000000-0000-4000-8000-000000000001', 'pgTAP barista', 'barista', 'unused-in-test');

INSERT INTO staff_sessions (token, staff_id, org_id, location_id) VALUES
  ('73000000-0000-4000-8000-000000000001', '72000000-0000-4000-8000-000000000001',
   '70000000-0000-4000-8000-000000000001', '71000000-0000-4000-8000-000000000001'),
  ('73000000-0000-4000-8000-000000000002', '72000000-0000-4000-8000-000000000002',
   '70000000-0000-4000-8000-000000000001', '71000000-0000-4000-8000-000000000001');

-- Протухшая сессия менеджера: срок истёк
INSERT INTO staff_sessions (token, staff_id, org_id, location_id, expires_at)
VALUES ('73000000-0000-4000-8000-000000000003', '72000000-0000-4000-8000-000000000001',
        '70000000-0000-4000-8000-000000000001', '71000000-0000-4000-8000-000000000001',
        NOW() - INTERVAL '1 hour');

-- Веб-владелец для проверки 089 поверх 090 (auth.users — только суперролью)
INSERT INTO auth.users (id) VALUES ('74000000-0000-4000-8000-000000000002');
INSERT INTO organization_members (org_id, auth_user_id, role, is_active)
VALUES ('70000000-0000-4000-8000-000000000001',
        '74000000-0000-4000-8000-000000000002', 'owner', TRUE);

SET LOCAL ROLE authenticated;
SELECT set_config(
  'request.jwt.claims',
  '{"sub":"74000000-0000-4000-8000-000000000001","role":"authenticated","app_metadata":{"org_id":"70000000-0000-4000-8000-000000000001","location_id":"71000000-0000-4000-8000-000000000001"}}',
  true
);

-- ── Ядро 090: без токена — отказ, а не тихий проход ──────────
SELECT throws_ok(
  $$ SELECT require_staff_perm(NULL, 'manage') $$,
  'P0001', 'staff session required',
  'вызов без токена сессии отклоняется (строгий режим)'
);

SELECT throws_ok(
  $$ SELECT require_staff_perm(NULL, 'refund') $$,
  'P0001', 'staff session required',
  'строгость не зависит от конкретного права'
);

SELECT throws_ok(
  $$ SELECT require_staff_perm('73000000-0000-4000-8000-0000000000ff', 'manage') $$,
  'P0001', 'staff session invalid',
  'неизвестный токен по-прежнему отклоняется'
);

SELECT throws_ok(
  $$ SELECT require_staff_perm('73000000-0000-4000-8000-000000000003', 'manage') $$,
  'P0001', 'staff session invalid',
  'протухшая сессия отклоняется'
);

-- ── Рабочий путь не сломан ───────────────────────────────────
SELECT lives_ok(
  $$ SELECT require_staff_perm('73000000-0000-4000-8000-000000000001', 'manage') $$,
  'менеджер с валидной сессией проходит'
);

SELECT is(
  require_staff_perm('73000000-0000-4000-8000-000000000001', 'manage'),
  '72000000-0000-4000-8000-000000000001'::uuid,
  'функция возвращает id сотрудника — автора операции'
);

-- Ролевой гейт: бариста не проходит менеджерское право
SELECT throws_ok(
  $$ SELECT require_staff_perm('73000000-0000-4000-8000-000000000002', 'manage') $$,
  'P0001', 'forbidden: manage',
  'бариста не получает менеджерское право'
);

-- Фолбэк 055 сохранён: stock_take остаётся менеджерским
SELECT throws_ok(
  $$ SELECT require_staff_perm('73000000-0000-4000-8000-000000000002', 'stock_take') $$,
  'P0001', 'forbidden: stock_take',
  'stock_take остаётся менеджерским (фолбэк 055 не потерян)'
);

-- ── 089 поверх 090: веб-владелец не упирается в строгий режим ──
-- Гейт бэкофиса стоит ДО require_staff_perm, поэтому «Обзор» в кабинете
-- продолжает работать без PIN-сессии. (auth.users и членство заведены
-- в фикстуре до SET ROLE — authenticated не пишет в auth.users.)
SELECT set_config(
  'request.jwt.claims',
  '{"sub":"74000000-0000-4000-8000-000000000002","role":"authenticated","app_metadata":{"org_id":"70000000-0000-4000-8000-000000000001"}}',
  true
);

SELECT lives_ok(
  $$ SELECT sales_report(NOW() - INTERVAL '1 day', NOW() + INTERVAL '1 day') $$,
  'владелец бэкофиса получает отчёт и в строгом режиме'
);

RESET ROLE;
SELECT * FROM finish();
ROLLBACK;
