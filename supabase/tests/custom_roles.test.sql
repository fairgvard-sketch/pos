-- pgTAP: кастомные роли (094).
--
-- Главный инвариант — РЕГРЕСС: при role_id IS NULL require_staff_perm ведёт
-- себя ровно как до 094 (строгий режим 090 + фолбэк-уровни 055). Эта функция
-- уже один раз молча откатывалась (см. 090), поэтому старое поведение
-- проверяется здесь наравне с новым.
--
-- Дальше — новая ветка: роль разрешает своё, запрещает не своё, владельца не
-- ограничивает, 'manage' через роль не выдаётся.

BEGIN;
SELECT plan(14);

-- ── Фикстура ────────────────────────────────────────────────
INSERT INTO orgs (id, name) VALUES
  ('b0000000-0000-4000-8000-000000000001', 'pgTAP org R1');

INSERT INTO locations (id, org_id, name) VALUES
  ('b1000000-0000-4000-8000-000000000001', 'b0000000-0000-4000-8000-000000000001', 'Loc R1');

INSERT INTO devices (id, org_id, location_id, name) VALUES
  ('b4000000-0000-4000-8000-000000000001', 'b0000000-0000-4000-8000-000000000001',
   'b1000000-0000-4000-8000-000000000001', 'Kassa R1');

-- Роль «Старший бариста»: возвраты можно, закрытие смены — нет
INSERT INTO roles (id, org_id, name, base, perms) VALUES
  ('b5000000-0000-4000-8000-000000000001', 'b0000000-0000-4000-8000-000000000001',
   'Старший бариста', 'barista', '["refund","discount"]'::JSONB);

INSERT INTO staff (id, org_id, location_id, name, role, pin_hash, is_active, role_id) VALUES
  -- Обычный бариста БЕЗ кастомной роли — контроль регресса
  ('b2000000-0000-4000-8000-000000000001', 'b0000000-0000-4000-8000-000000000001',
   'b1000000-0000-4000-8000-000000000001', 'Бариста', 'barista', 'x', TRUE, NULL),
  -- Бариста С кастомной ролью
  ('b2000000-0000-4000-8000-000000000002', 'b0000000-0000-4000-8000-000000000001',
   'b1000000-0000-4000-8000-000000000001', 'Старший', 'barista', 'x', TRUE,
   'b5000000-0000-4000-8000-000000000001'),
  -- Владелец с той же ограниченной ролью — роль его ограничивать не должна
  ('b2000000-0000-4000-8000-000000000003', 'b0000000-0000-4000-8000-000000000001',
   'b1000000-0000-4000-8000-000000000001', 'Владелец', 'owner', 'x', TRUE,
   'b5000000-0000-4000-8000-000000000001');

INSERT INTO staff_sessions (token, staff_id, org_id, location_id) VALUES
  ('b3000000-0000-4000-8000-000000000001', 'b2000000-0000-4000-8000-000000000001',
   'b0000000-0000-4000-8000-000000000001', 'b1000000-0000-4000-8000-000000000001'),
  ('b3000000-0000-4000-8000-000000000002', 'b2000000-0000-4000-8000-000000000002',
   'b0000000-0000-4000-8000-000000000001', 'b1000000-0000-4000-8000-000000000001'),
  ('b3000000-0000-4000-8000-000000000003', 'b2000000-0000-4000-8000-000000000003',
   'b0000000-0000-4000-8000-000000000001', 'b1000000-0000-4000-8000-000000000001');

SET LOCAL ROLE authenticated;
-- Токен устройства: org + location (как у кассы)
SELECT set_config(
  'request.jwt.claims',
  '{"sub":"b4000000-0000-4000-8000-000000000001","role":"authenticated","app_metadata":{"org_id":"b0000000-0000-4000-8000-000000000001","location_id":"b1000000-0000-4000-8000-000000000001"}}',
  true
);

-- ============================================================
-- РЕГРЕСС: role_id IS NULL — поведение как до 094
-- ============================================================
SELECT throws_ok(
  $$ SELECT require_staff_perm(NULL, 'discount') $$,
  'staff session required',
  'строгий режим 090 сохранён: без токена отказ'
);
SELECT throws_ok(
  $$ SELECT require_staff_perm('b3000000-0000-4000-8000-0000000000ff', 'discount') $$,
  'staff session invalid',
  'битый токен отклоняется'
);
SELECT is(
  require_staff_perm('b3000000-0000-4000-8000-000000000001', 'discount'),
  'b2000000-0000-4000-8000-000000000001'::UUID,
  'бариста без роли: discount по умолчанию all — разрешено'
);
SELECT throws_ok(
  $$ SELECT require_staff_perm('b3000000-0000-4000-8000-000000000001', 'refund') $$,
  'forbidden: refund',
  'бариста без роли: refund менеджерский — запрещено (фолбэк 055)'
);
SELECT throws_ok(
  $$ SELECT require_staff_perm('b3000000-0000-4000-8000-000000000001', 'stock_take') $$,
  'forbidden: stock_take',
  'бариста без роли: stock_take менеджерский — запрещено'
);

-- Уровни из настроек точки продолжают действовать.
-- Прямой UPDATE locations закрыт грантами (045), поэтому правим из-под
-- владельца БД и возвращаем роль обратно.
RESET ROLE;
UPDATE locations SET settings = '{"perms":{"discount":"manager"}}'::JSONB
WHERE id = 'b1000000-0000-4000-8000-000000000001';
SET LOCAL ROLE authenticated;

SELECT throws_ok(
  $$ SELECT require_staff_perm('b3000000-0000-4000-8000-000000000001', 'discount') $$,
  'forbidden: discount',
  'настройка точки perms.discount=manager по-прежнему запрещает бариста'
);

RESET ROLE;
UPDATE locations SET settings = '{}'::JSONB
WHERE id = 'b1000000-0000-4000-8000-000000000001';
SET LOCAL ROLE authenticated;

-- ============================================================
-- НОВОЕ: кастомная роль
-- ============================================================
SELECT is(
  require_staff_perm('b3000000-0000-4000-8000-000000000002', 'refund'),
  'b2000000-0000-4000-8000-000000000002'::UUID,
  'роль разрешает refund бариста, хотя фолбэк его запрещает'
);
SELECT is(
  require_staff_perm('b3000000-0000-4000-8000-000000000002', 'discount'),
  'b2000000-0000-4000-8000-000000000002'::UUID,
  'роль разрешает discount'
);
SELECT throws_ok(
  $$ SELECT require_staff_perm('b3000000-0000-4000-8000-000000000002', 'close_shift') $$,
  'forbidden: close_shift',
  'роль НЕ разрешает close_shift — запрещено, хотя по умолчанию было all'
);
SELECT throws_ok(
  $$ SELECT require_staff_perm('b3000000-0000-4000-8000-000000000002', 'manage') $$,
  'forbidden: manage',
  'управление сотрудниками через кастомную роль не выдаётся'
);

-- Владелец не ограничивается ролью
SELECT is(
  require_staff_perm('b3000000-0000-4000-8000-000000000003', 'close_shift'),
  'b2000000-0000-4000-8000-000000000003'::UUID,
  'владелец с ограниченной ролью всё равно может close_shift'
);
SELECT is(
  require_staff_perm('b3000000-0000-4000-8000-000000000003', 'manage'),
  'b2000000-0000-4000-8000-000000000003'::UUID,
  'владелец сохраняет manage при назначенной роли'
);

-- ============================================================
-- save_role: 'manage' и мусорные ключи вырезаются
-- ============================================================
RESET ROLE;
SELECT set_config(
  'request.jwt.claims',
  '{"sub":"b4000000-0000-4000-8000-000000000001","role":"authenticated","app_metadata":{"org_id":"b0000000-0000-4000-8000-000000000001","location_id":"b1000000-0000-4000-8000-000000000001"}}',
  true
);
SET LOCAL ROLE authenticated;

CREATE TEMP TABLE saved AS
SELECT save_role('Кассир', 'barista',
                 '["refund","manage","bogus_key"]'::JSONB, NULL,
                 'b3000000-0000-4000-8000-000000000003') AS id;

SELECT is(
  (SELECT perms FROM roles WHERE id = (SELECT id FROM saved)),
  '["refund"]'::JSONB,
  'save_role вырезает manage и неизвестные ключи'
);

SELECT throws_ok(
  $$ SELECT save_role('Плохая база', 'owner', '[]'::JSONB, NULL,
                      'b3000000-0000-4000-8000-000000000003') $$,
  'invalid base role',
  'base=owner запрещён: владельца назначают только сменой staff.role'
);

SELECT * FROM finish();
ROLLBACK;
